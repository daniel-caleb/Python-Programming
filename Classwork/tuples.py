#simple program on tuples
x=(3,5,2,9,10,7) #tuple elements
print('\n----Printing tuple items--')
i=0 #initialize
while (i <6): #use loop to iterate
    print(x[i],end=',')
    i=i+1 #update
print('\n----End------------')

#simple program on tuples
x=(3,5,2,9,10,7) #tuple elements
print('\n----Printing tuple items in reverse--')
i=5 #initialize
